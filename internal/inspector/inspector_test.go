package inspector

import (
	"testing"
)

func TestIspector_Audit(t *testing.T) {
	ins := New()
	tests_valid := []struct {
		name string
		args OrderBox
	}{
		{"ord_valid", OrderBox{Data: []byte(ord_valid)}},
	}
	tests_invalid := []struct {
		name string
		args OrderBox
	}{
		{"ord_not_key", OrderBox{Data: []byte(ord_not_key)}},
		{"ord_not_items", OrderBox{Data: []byte(ord_not_items)}},
	}
	for _, tt := range tests_valid {
		t.Run(tt.name, func(t *testing.T) {
			newBox := ins.Audit(tt.args)
			if newBox.Err != nil {
				t.Error(newBox.Err)
			}
		})
	}
	for _, tt := range tests_invalid {
		t.Run(tt.name, func(t *testing.T) {
			newBox := ins.Audit(tt.args)
			if newBox.Err == nil {
				t.Errorf("Result no error in %s", tt.name)
			}
		})
	}
}

